# Over the summer, temperatures in Yorkshire reached 38.4C. Write a program that
# converts this value in Celsius to the equivalent temperature in Fahrenheit, and then
# displays both.

Yorkshire_temp=38.4
farhen_temp=round((Yorkshire_temp * 9 /5) + 32 , 2 )
print(f"The temperature of Yorkshire in celcius is {Yorkshire_temp}°C and in farhenheit is {farhen_temp}°F")
